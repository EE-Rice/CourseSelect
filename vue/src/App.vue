<!--
  ============================================
  App.vue - 根组件（单文件组件 .vue 格式）
  ============================================
  .vue 文件是 Vue 的特色，一个文件包含 HTML + JS + CSS
  三部分：<template>（模板）、<script>（逻辑）、<style>（样式）

  这个组件是所有页面的容器，本身不显示具体内容
  具体内容由 <router-view /> 动态替换
-->

<!-- ============================================
     第一部分：<template> - HTML 模板
     ============================================ -->
<template>
  <!-- Vue Router 提供的特殊组件
  它是"占位符"，URL 自动显示对应的页面组件
      <router-view />
   -->
  <router-view />
</template>

<!-- ============================================
     第二部分：<script setup> - 逻辑脚本
     ============================================ -->
<script setup> // Vue 3 的"组合式 API"语法糖，让代码更简洁，不用写 export default 和 return
// 暂无全局逻辑
</script>


<!-- ============================================
     第三部分：<style> - CSS 样式
     ============================================ -->
<style>
/* 全局样式 */
/* 重置所有元素的内外边距，统一盒模型 */
* {
  margin: 0; /* 外边距置零 */
  padding: 0; /* 内边距置零 */
  box-sizing: border-box; /* 盒模型：宽高包含边框和内边距 */
}

/* 设置全局字体 */
body {
  font-family: 'Helvetica Neue', Helvetica, 'PingFang SC', 'Microsoft YaHei', sans-serif;
}
</style>